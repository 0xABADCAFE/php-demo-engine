namespace ABadCafe\PDE\Util;
/**
 * Interface for classes that abstracts away the semantics of whether or not an instance is sharable. To be sharable,
 * an instance is generally required to be stateless or at least idempotent.
 *
 */
interface ISometimesShareable {

    /**
     * Returns an instance. This may or may not be the thing being called depending on whether or not it's sharable.
     */
    public function share(): self;

}
