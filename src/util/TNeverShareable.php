declare(strict_types=1);

namespace ABadCafe\PDE\Util;

/**
 * TNeverShareable
 *
 * Mixin for ISometimesSharable implementors that always want copy semantics.
 */
trait TNeverShareable {

    /**
     * @return self
     */
    public function share(): self {
        return clone $this;
    }
}
